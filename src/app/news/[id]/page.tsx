interface NewsDetailPageProps {
  params: {
    id: string;
  };
}

export default function NewsDetailPage({ params }: NewsDetailPageProps) {
  const newsId = parseInt(params.id);
  const news = mockNews.find(item => item.id === newsId);

  if (!news) {
    notFound();
  }

  const formatDate = (dateString: string) => {
    const date = new Date(dateString);
    return date.toLocaleDateString('ru-RU', {
      day: 'numeric',
      month: 'long',
      year: 'numeric'
    });
  };

  return (
    <>
      {/* Hero Section with Background Image */}
      <section className="relative min-h-screen flex items-center">
        {/* Background Image */}
        <div className="absolute inset-0 z-0">
          <Image
            src={news.image}
            alt={news.title}
            fill
            className="object-cover"
            priority
          />
          <div className="absolute inset-0 bg-black/70"></div>
        </div>

        {/* Content */}
        <div className="relative z-10 w-full">
          <div className="max-w-base mx-auto px-4 md:px-12 lg:px-4 pt-32 pb-16">
            <div className="max-w-4xl">
              {/* Back Button */}
              <Link 
                href="/news"
                className="inline-flex items-center gap-2 text-brand-brown-100 hover:text-brand-brown transition-colors duration-200 mb-8"
              >
                <svg width="20" height="20" viewBox="0 0 20 20" fill="none" xmlns="http://www.w3.org/2000/svg">
                  <path d="M15.8333 10H4.16667M4.16667 10L10 15.8333M4.16667 10L10 4.16667" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/>
                </svg>
                Назад к новостям
              </Link>

              {/* Date */}
              <div className="mb-6">
                <span className="text-brand-brown-100 text-lg font-medium">
                  {formatDate(news.date)}
                </span>
              </div>

              {/* Title */}
              <h1 className="text-4xl md:text-6xl lg:text-7xl font-oswald font-medium uppercase text-white leading-tight mb-8">
                {news.title}
              </h1>

              {/* Description */}
              <p className="text-xl md:text-2xl text-brand-dark-gray-200 leading-relaxed max-w-3xl">
                {news.description}
              </p>
            </div>
          </div>
        </div>
      </section>

      {/* Full Content Section */}
      <section className="relative bg-brand-dark-gray py-16">
        <div className="max-w-base mx-auto relative z-10 px-4 md:px-12 lg:px-4">
          <div className="max-w-4xl mx-auto">
            <div className="prose prose-lg prose-invert max-w-none">
              <div className="text-white text-lg leading-relaxed whitespace-pre-line">
                {news.fullContent}
              </div>
            </div>

            {/* Back to News Button */}
            <div className="text-center mt-16">
              <Link 
                href="/news"
                className="inline-flex items-center gap-2 bg-brand-brown-100 text-white px-8 py-4 font-normal hover:bg-brand-brown transition-colors duration-200"
              >
                Вернуться к новостям
              </Link>
            </div>
          </div>
        </div>
      </section>
    </>
  );
} 